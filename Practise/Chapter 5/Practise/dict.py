# Now we're going to learn about dictionary in Python
# As per my experience Dictionary in python is similar to objects in JS
# They are enclosed in {} and the values are represented in {key:value} pairs
dict = { 'name': 'John', 'age':32, 'hobby': ['reading', 'coding', 'cycling']  }
# dict methods are as follows:
dict.items() # return list of key value pairs
dict.keys()
dict.update()  # {"age":43}
dict.get("hobby")